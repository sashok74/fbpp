ConnectionParams makeScopedTestDatabaseParams(std::string_view scope,
                                              std::string_view section) {
    ConnectionParams params = fbpp::util::getConnectionParams(std::string(section));

    const auto separator = params.database.find(':');
    if (separator == std::string::npos) {
        params.database = addScopeSuffix(params.database, scope);
        return params;
    }

    const std::string prefix = params.database.substr(0, separator);
    const std::string path = params.database.substr(separator + 1);
    params.database = prefix + ":" + addScopeSuffix(path, scope);
    return params;
}

void recreateTestDatabase(const ConnectionParams& params) {
    Connection::dropDatabase(params);
    Connection::createDatabase(params);
}

void dropTestDatabaseQuietly(const ConnectionParams& params) noexcept {
    try {
        Connection::dropDatabase(params);
    } catch (...) {
        // Best-effort cleanup only.
    }
}

void applySchemaProfiles(Connection& connection, const std::vector<SchemaProfile>& profiles) {
    for (const auto profile : profiles) {
        applySchemaProfile(connection, profile);
    }
}

void ManagedDatabaseTest::initializeDatabase() {
    if (!connection_) {
        throw std::runtime_error("ManagedDatabaseTest requires an active connection");
    }

    applySchemaProfiles(*connection_, schemaProfiles());
    createTestSchema();
    seedTestData();
}

void SuiteDatabaseTest::SetUp() {
    const auto* testInfo = ::testing::UnitTest::GetInstance()->current_test_info();
    if (!testInfo) {
        throw std::runtime_error("Unable to resolve current test info");
    }

    auto& registry = suiteDatabaseRegistry();
    {
        std::lock_guard<std::mutex> lock(registry.mutex);
        auto it = registry.paramsBySuite.find(testInfo->test_suite_name());
        if (it == registry.paramsBySuite.end()) {
            ConnectionParams params = makeScopedTestDatabaseParams(buildSuiteScope(testInfo));
            recreateTestDatabase(params);

            try {
                db_params_ = params;
                connection_ = std::make_unique<Connection>(db_params_);
                initializeDatabase();
                connection_.reset();
            } catch (...) {
                connection_.reset();
                dropTestDatabaseQuietly(params);
                throw;
            }

            it = registry.paramsBySuite.emplace(testInfo->test_suite_name(), params).first;
        }

        db_params_ = it->second;
    }

    connection_ = std::make_unique<Connection>(db_params_);
}

void SuiteDatabaseTest::TearDownTestSuite() {
    const auto* suite = ::testing::UnitTest::GetInstance()->current_test_suite();
    if (!suite) {
        return;
    }

    auto& registry = suiteDatabaseRegistry();
    ConnectionParams params;
    bool found = false;
    {
        std::lock_guard<std::mutex> lock(registry.mutex);
        auto it = registry.paramsBySuite.find(suite->name());
        if (it != registry.paramsBySuite.end()) {
            params = it->second;
            registry.paramsBySuite.erase(it);
            found = true;
        }
    }

    if (found) {
        dropTestDatabaseQuietly(params);
    }
}

void SuiteDatabaseTest::TearDown() {
    connection_.reset();
}

void TempDatabaseTest::SetUp() {
    const auto* testInfo = ::testing::UnitTest::GetInstance()->current_test_info();
    if (!testInfo) {
        throw std::runtime_error("Unable to resolve current test info");
    }

    db_params_ = makeScopedTestDatabaseParams(buildTestScope(testInfo, test_counter_++));
    recreateTestDatabase(db_params_);
    connection_ = std::make_unique<Connection>(db_params_);
    initializeDatabase();
}

void TempDatabaseTest::TearDown() {
    connection_.reset();
    dropTestDatabaseQuietly(db_params_);
}

void TempDatabaseTest::createTestSchema() {
    applySchemaProfiles(*connection_, {SchemaProfile::BasicTable});
}

int TempDatabaseTest::test_counter_ = 0;

