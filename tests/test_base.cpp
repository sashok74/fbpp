std::string buildSuiteScope(const ::testing::TestInfo* testInfo) {
    return std::string(testInfo->test_suite_name()) + "_" +
           std::to_string(currentProcessId());
}

std::string buildTestScope(const ::testing::TestInfo* testInfo, const int counter) {
    return std::string(testInfo->test_suite_name()) + "_" +
           testInfo->name() + "_" +
           std::to_string(currentProcessId()) + "_" +
           std::to_string(counter);
}

