std::string normalizeScope(std::string_view raw) {
    std::string normalized;
    normalized.reserve(raw.size());

    bool lastWasUnderscore = false;
    for (const char ch : raw) {
        const unsigned char uch = static_cast<unsigned char>(ch);
        if (std::isalnum(uch)) {
            normalized.push_back(static_cast<char>(std::tolower(uch)));
            lastWasUnderscore = false;
        } else if (!lastWasUnderscore) {
            normalized.push_back('_');
            lastWasUnderscore = true;
        }
    }

    while (!normalized.empty() && normalized.front() == '_') {
        normalized.erase(normalized.begin());
    }
    while (!normalized.empty() && normalized.back() == '_') {
        normalized.pop_back();
    }

    if (normalized.empty()) {
        normalized = "test_db";
    }

    return normalized;
}

long currentProcessId() {
#if defined(_WIN32)
    return static_cast<long>(_getpid());
#else
    return static_cast<long>(getpid());
#endif
}

std::string addScopeSuffix(std::string path, std::string_view scope) {
    const std::string suffix = "_" + normalizeScope(scope);
    const auto dotPos = path.find_last_of('.');
    if (dotPos != std::string::npos) {
        path.insert(dotPos, suffix);
        return path;
    }
    path += suffix;
    return path;
}

void executeDdlBatch(Connection& connection, std::initializer_list<const char*> statements) {
    for (const char* statement : statements) {
        connection.ExecuteDDL(statement);
    }
}

